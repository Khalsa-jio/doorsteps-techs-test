describe("experiment", () => {
  it("user can make experiment", () => {
    cy.visit("http://localhost:3001/")
    cy.findByRole("link", { name: /create experiment/i }).click()
    cy.findByRole("textbox", { name: /name/i }).type("test_experiment")
    cy.findByRole("textbox", { name: /experiment description/i }).type("test_experiment_description")
    cy.findByRole("button", { name: /add new question/i }).click()
    cy.get(".questionForm").should("be.visible")
    cy.findByRole("textbox", { name: /field_label/i })
      .should("be.visible")
      .type("test_field_label")
    cy.findByRole("textbox", { name: /field_id/i })
      .should("be.visible")
      .should("be.empty")
      .type("test_field_id")
    cy.findByRole("textbox", { name: /field_mandatory/i }).type("yes")
    cy.findByRole("textbox", { name: /field_placeholder/i }).type("test_field_placeholder")
    cy.findByRole("textbox", { name: /field_value/i }).type("test_field_value")
    cy.findByRole("combobox").select("select")
    cy.findByRole("textbox", { name: /field_options/i })
      .should("be.visible")
      .type("options 1,options 2,options 3")
    cy.findByRole("button", { name: /submit/i }).click()
    cy.findByRole("button", { name: /submit/i }).click()

    // cy.findByRole("button", { name: /submit/i })
    // cy.findByRole("textbox", { name: /name/i }).should("have.value", "test_experiment")
    // cy.findByRole("textbox", { name: /experiment description/i }).should("have.value", "test_experiment_description")
  })
})
